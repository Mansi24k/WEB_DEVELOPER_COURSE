//Math.Random()
console.log(Math.random());
console.log(Math.random()*100);
console.log(Math.random()*1000);

//Math.floor()
console.log(Math.floor(10.5));//10
console.log(Math.floor(15.3));//15

//Math.ceil()
console.log(Math.ceil(10.1));//11
console.log(Math.ceil(15.65));//16
console.log(Math.ceil(5));//5

//Math.round()
console.log(Math.round(10.1));//10
console.log(Math.round(15.5));//16
console.log((Math.round(15.65)));//16
console.log(Math.round(5));//5

//Math.trunc()
console.log(Math.trunc(10.1));//10
console.log(Math.trunc(15.5));//15
console.log((Math.trunc(15.65)));//15
console.log(Math.trunc(5));//5

//Math.tan()
console.log(Math.tan(1));//1.5574077246549023
console.log(Math.tan(1).toFixed(2));//1.56 //.tofixed()
console.log(Math.tan(1).toFixed(3));//1.557

//Math.pow()
console.log(Math.pow(3,6));//729
console.log(Math.pow(2,2));//4

//Math.PI()
console.log(Math.PI);//3.141592653589793
console.log(Math.PI.toFixed(2));//3.14